#include <stdint.h>
#include <string.h>
#include <stdbool.h>

#include "fsr_adc.h"

#include "app_error.h"
#include "fsr_ble.h"
#include "nrf_soc.h"

//#define USE_LOG

#ifdef USE_LOG
    #include "nrf_log_ctrl.h"
#endif

#define DEAD_BEEF   0xDEADBEEF  /**< Value used as error code on stack dump, can be used to identify stack location on stack unwind. */

/**@brief Function for assert macro callback.
 *
 * @details This function will be called in case of an assert in the SoftDevice.
 *
 * @warning This handler is an example only and does not fit a final product. You need to analyse
 *          how your product is supposed to react in case of Assert.
 * @warning On assert from the SoftDevice, the system can only recover on reset.
 *
 * @param[in] line_num    Line number of the failing ASSERT call.
 * @param[in] p_file_name File name of the failing ASSERT call.
 */
void assert_nrf_callback(uint16_t line_num, const uint8_t * p_file_name)
{
    app_error_handler(DEAD_BEEF, line_num, p_file_name);
}

/**@brief Function for placing the application in low power state while waiting for events.
 */

#define FPU_EXCEPTION_MASK 0x0000009F

static void power_manage(void)
{
    __set_FPSCR(__get_FPSCR()  & ~(FPU_EXCEPTION_MASK));
    (void) __get_FPSCR();
    NVIC_ClearPendingIRQ(FPU_IRQn);

    uint32_t err_code = sd_app_evt_wait();
    APP_ERROR_CHECK(err_code);
}

/**@brief Application main function.
 */
int main(void)
{
    fsr_ble_init();
    for (;;)
    {
        //Check if ADC is done a sample and send a notification. Notification will finish before returning
        check_saadc_done_sample();
        //Check if the ADC needs calibration. Calibration will finish before returning
        check_saadc_calibration();

#ifdef USE_LOG
        if (!NRF_LOG_PROCESS())
        {
            power_manage();
        }
#else
        power_manage();
#endif
    }
}

